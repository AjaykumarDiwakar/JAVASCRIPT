// if
if(true){
    console.log("Inside if");
}

if(false){
    console.log("Never run");
    
}
// if-else
if(''){
    console.log("Condition is true");
}
else{
    console.log("Condition is false");
}

// if-else ladder
if(''){
    console.log("First condition('') is true");
} else if(null){
    console.log("Second condition(null) is true");
}else if(undefined){
    console.log("Third condition(undefined) is true");
}else if(0){
    console.log("Fourth condition(0) is true");
}else{
    console.log("no condition is true")
}